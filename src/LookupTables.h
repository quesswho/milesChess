    static std::array<llu, 64 * 64> InitActiveMoves() {
        std::array<llu, 64 * 64> result;
        for (int ksq = 0; ksq < 64; ksq++) {
            for (int enemysq = 0; enemysq < 64; enemysq++) {
                if (ksq == enemysq) {
                    result[ksq * 64 + enemysq] = 0;
                    continue;
                }
                uint64 kingb = 1ull << ksq;
                uint64 enemyb = 1ull << enemysq;
                if ((lines[ksq*4+2] & enemyb) > 0) { // There is a diagonal attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 9;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 9;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else if ((lines[ksq * 4 + 3] & enemyb) > 0) { // There is a anti diagonal attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 7;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 7;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                } else if ((lines[ksq * 4] & enemyb) > 0) { // There is a file attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 8;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 8;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else if ((lines[ksq * 4 + 1] & enemyb) > 0) { // There is a file attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 1;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 1;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else {
                    result[ksq * 64 + enemysq] = 0;
                }
            }
        }
        return result;
    }

