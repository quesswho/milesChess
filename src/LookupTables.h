    static std::array<llu, 64 * 64> InitActiveMoves() {
        std::array<llu, 64 * 64> result;
        for (int ksq = 0; ksq < 64; ksq++) {
            for (int enemysq = 0; enemysq < 64; enemysq++) {
                if (ksq == enemysq) {
                    result[ksq * 64 + enemysq] = 0;
                    continue;
                }
                uint64 kingb = 1ull << ksq;
                uint64 enemyb = 1ull << enemysq;
                if ((lines[ksq * 4 + 2] & enemyb) > 0) { // There is a diagonal attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 9;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 9;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else if ((lines[ksq * 4 + 3] & enemyb) > 0) { // There is a anti diagonal attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 7;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 7;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else if ((lines[ksq * 4] & enemyb) > 0) { // There is a file attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 8;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 8;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else if ((lines[ksq * 4 + 1] & enemyb) > 0) { // There is a file attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 1;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 1;
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else {
                    result[ksq * 64 + enemysq] = 0;
                }
            }
        }
        return result;
    }

    static std::array<llu, 64 * 64> InitCheckMoves() {
        std::array<llu, 64 * 64> result;
        for (int ksq = 0; ksq < 64; ksq++) {
            for (int enemysq = 0; enemysq < 64; enemysq++) {
                if (ksq == enemysq) {
                    result[ksq * 64 + enemysq] = 0;
                    continue;
                }
                if (ksq * 64 + enemysq == 664) {
                    //printf("");
                }
                uint64 kingb = 1ull << ksq;
                uint64 enemyb = 1ull << enemysq;
                if ((lines[ksq * 4 + 2] & enemyb) > 0) { // There is a diagonal attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 9;
                        }
                        if (((kingb & lines[7 * 4]) | (kingb & lines[8 * 7 * 4 + 1])) == 0) {
                            enemyb <<= 9;
                            active |= enemyb; // One square behind the king
                        }
                        
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 9;
                        }
                        if (((kingb & lines[0]) | (kingb & lines[1])) == 0) {
                            enemyb >>= 9;
                            active |= enemyb; // One square behind the king
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else if ((lines[ksq * 4 + 3] & enemyb) > 0) { // There is a anti diagonal attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 7;
                        }
                        if (((kingb & lines[0]) | (kingb & lines[8 * 7 * 4 + 1])) == 0) {
                            enemyb <<= 7;
                            active |= enemyb; // One square behind the king
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 7;
                        }
                        if (((kingb & lines[7 * 4]) | (kingb & lines[1])) == 0) {
                            enemyb >>= 7;
                            active |= enemyb; // One square behind the king
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                } else if ((lines[ksq * 4] & enemyb) > 0) { // There is a file attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 8;
                        }
                        if ((kingb & lines[8 * 7 * 4 + 1]) == 0) {
                            enemyb <<= 8;
                            active |= enemyb; // One square behind the king
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 8;
                        }
                        if ((kingb & lines[1]) == 0) {
                            enemyb >>= 8;
                            active |= enemyb; // One square behind the king
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else if ((lines[ksq * 4 + 1] & enemyb) > 0) { // There is a rank attack
                    if (ksq > enemysq) {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb <<= 1;
                        }
                        if ((kingb & lines[7 * 4]) == 0) {
                            enemyb <<= 1;
                            active |= enemyb; // One square behind the king
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                    else {
                        uint64 active = 0;
                        while (enemyb != kingb) {
                            active |= enemyb;
                            enemyb >>= 1;
                        }
                        if ((kingb & lines[0]) == 0) {
                            enemyb >>= 1;
                            active |= enemyb; // One square behind the king
                        }
                        result[ksq * 64 + enemysq] = active;
                    }
                }
                else {
                    result[ksq * 64 + enemysq] = 0;
                }
            }
        }
        return result;
    }

